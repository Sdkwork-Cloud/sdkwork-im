export function repairFffdContexts(source) {
  let next = source;
  for (const [from, to] of FFFD_CONTEXT_REPAIRS) {
    next = next.split(from).join(to);
  }
  // Remaining isolated strip pairs at line ends usually were full-width punctuation.
  next = next.replace(/\uFFFD\?(?=\s*$)/gmu, '。');
  next = next.replace(/\uFFFD\?/gu, '');
  return next;
}
